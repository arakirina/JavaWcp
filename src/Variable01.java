
public class Variable01 {
	public static void main(String[] args) {
		int num1;
//		int型の変数num1を宣言する
		String str1;
//		string型の変数str1を宣言する
		num1 = 10;
//		num1を初期化(10を代入)
		str1 = "Hello World!";
//		str1を初期化(Hello Worldを代入)
		System.out.println(num1);
		System.out.println(str1);
//		num1、str1を出力
		
		num1 = 20;
//		num1に20を再代入
		System.out.println(num1);
		
		int num2 = 30;
//		int型の変数num2を宣言と同時に初期化(30を代入)
		System.out.println(num2);
	}
}
